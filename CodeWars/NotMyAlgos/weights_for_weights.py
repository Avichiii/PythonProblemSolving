def sum_string(digits):
    sum_ = 0
    for i in digits:
        sum_ += int(i)
    return sum_

def order_weight(strng):
    first_sort_alphabetically = sorted(str(strng).split())
    final_sort_digit_sum = sorted(first_sort_alphabetically, key=sum_string)
    return ' '.join(final_sort_digit_sum)


        
